// Change profile picture when user hovers over it.
var numHovers = 0;
$('.profile-pic').on("mouseenter click", function(){
  var hoverPic = 'images/profile-pic-b-' + (numHovers % 6) + '.jpg'
  $(this).attr('src', hoverPic);
  numHovers++;
});

